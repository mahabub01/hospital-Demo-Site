<h2>About Us</h2>
<p>The CARE Hospitals Group is a multispecialty healthcare provider, with 14 hospitals serving 6 cities across 5 states of India. The regional leader in tertiary care in South/Central India and among the top 4 pan-Indian hospital chains, CARE Hospitals delivers comprehensive care in more than 30 specialties in tertiary care settings.</p>

<p>Adopting a service-oriented delivery model, CARE Hospitals provides cost-effective medical care, with an uncompromising commitment to its core purpose, “To provide care that people trust.” Founded in 1997 by Dr B Soma Raju and a team of India’s leading cardiologists, the CARE Hospitals’ founders developed Asia’s first indigenous coronary stent — the ‘Kalam-Raju stent,’ named after Dr APJ Abdul Kalam, former President of India, and Dr B Soma Raju, Chairman and Managing Director, CARE Hospitals Group. This is just one of the many innovations developed at CARE Hospitals, which strives to make healthcare affordable for all, while ensuring clinical outcomes at par with international standards.</p>
<p>
The CARE team has many firsts to its credit:
<ul>
	<li>1st Prospective randomised trial of balloon valvotomy vs </li>
	<li>mitral valve replacement in the world</li>
	<li>1st Coronary Angioplasty in India</li>
	<li>2nd Balloon Valvuloplasty in India</li>
	<li>1st Cardiac MRI in India</li>
	<li>1st Atrial Fibrillation Clinic in India</li>
	<li>1st Beating Heart Surgery in Andhra Pradesh</li>
	<li>1st Heart Transplant Performed in a tier-2 town (Visakhapatnam)</li>

</ul>
</p>
<p> Today the CARE Hospitals Group comprises 14 multi superspecialty hospitals offering comprehensive patient-focused care, with state-of-the-art infrastructure, medical equipment and facilities, complemented by globally trained and experienced doctors and professional support staff, available 24*7. It is recognized as the most advanced, complete cardiac care centre in the country, powered by cross-functional teams with deeper skill sets and round-the-clock availability of cardiologists, cardiac surgeons, critical care and emergency medicine specialists. The hospitals are models for high doctor-patient and nurse-patient ratios to ensure patient satisfaction.</p>
<p>CARE Hospitals is a complete healthcare ecosystem, with related components of education/training and research, pursuing innovations that make healthcare more affordable. A network of telemedicine hubs in rural Andhra Pradesh and Maharashtra deliver care at the doorstep to lakhs of people. These activities are managed by its partner CARE Foundation.</p>
<p>CARE Hospitals continually updates medical knowledge and puts it into practice — taking innovations from the bench to the bedside. The Group has a rigorous Clinical Research programme (certified by the Drugs Controller General of India), with advanced infrastructure and trained resources to conduct clinical trials in the areas of drug and medical device development. Both pioneering original research and sponsored clinical trials are conducted at several hospitals and over 40 studies are under way, at any given time.</p>
